import type { ReactElement } from "react";

/**
 * Pixel-art icon kit. Each icon is a 16×16 grid of `#` (filled) / `.` (empty),
 * rendered as `<rect>`s with `shape-rendering="crispEdges"` so it stays crunchy
 * at any scale. `currentColor` flows through `fill` — set with Tailwind
 * `text-…` classes.
 */

export interface IconProps {
  size?: number;
  className?: string;
  title?: string;
}

const SIZE = 16;

function gridToRects(grid: string[]): ReactElement[] {
  const out: ReactElement[] = [];
  for (let y = 0; y < grid.length; y++) {
    const row = grid[y];
    for (let x = 0; x < row.length; x++) {
      if (row[x] === "#") {
        out.push(<rect key={`${x}-${y}`} x={x} y={y} width="1" height="1" />);
      }
    }
  }
  return out;
}

function makeIcon(label: string, grid: string[]) {
  const rects = gridToRects(grid);
  const Icon = ({ size = 16, className = "", title }: IconProps) => (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${SIZE} ${SIZE}`}
      shapeRendering="crispEdges"
      fill="currentColor"
      className={`shrink-0 ${className}`}
      role="img"
      aria-label={title ?? label}
      style={{ minWidth: size, minHeight: size }}
    >
      {rects}
    </svg>
  );
  Icon.displayName = `Pixel${label}`;
  return Icon;
}

